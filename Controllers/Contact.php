<?php

namespace App\Controllers;

class Contact extends BaseController
{
    protected $module;
    protected $path;

    function __construct()
    {
        $this->module  = "Contact";
        $this->path  = "";
    }

    public function index()
    {
        $data = [
            'module'    => $this->module,
            'path'      => $this->path,
            'act'       => 'Contact',
        ];
        return view($this->path . '/' . $this->module, $data);
    }

    public function contentContactDeskrispsi()
    {
        $result = json_decode($this->request->getVar("result"));
        $param = json_decode($this->request->getVar("param"));
        $data = [
            'module'    => $this->module,
            'path'      => $this->path,
            'act'       => 'contentContactDeskrispsi',
            'result'    => $result,
            'param'    => $param,
        ];
        return $this->response->setJSON(json_encode(["status" => true, "output" => view($this->path . '/' . $this->module, $data)]));
    }
}
